#include <iostream>

#include "Kaprekar.h"

int main()
{
    unsigned int number{0};
    while (std::cin >> number)
    {
        if (kaprekar::isRepdigit(number))
        {
            std::cout << "-1" << "\n";
            continue;
        }
        std::cout << kaprekar::countSteps(number) << "\n";
    }
    return 0;
}
